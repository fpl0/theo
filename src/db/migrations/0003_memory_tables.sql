INSERT INTO core_memory (slot, body) VALUES
  ('persona', '{}'),
  ('goals', '{}'),
  ('user_model', '{}'),
  ('context', '{}')
ON CONFLICT (slot) DO NOTHING;

-- ---------------------------------------------------------------------------
-- Core Memory Changelog: core_memory_changelog
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS core_memory_changelog (
  id          integer     GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slot        text        NOT NULL REFERENCES core_memory(slot),
  body_before jsonb       NOT NULL,
  body_after  jsonb       NOT NULL,
  changed_by  text        NOT NULL,  -- actor
  created_at  timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_core_memory_changelog_slot ON core_memory_changelog (slot);

-- ---------------------------------------------------------------------------
-- User Model: user_model_dimension
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS user_model_dimension (
  id             integer     GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name           text        NOT NULL UNIQUE,
  value          jsonb       NOT NULL DEFAULT '{}',
  confidence     real        NOT NULL DEFAULT 0.0
                 CHECK (confidence >= 0.0 AND confidence <= 1.0),
  evidence_count integer     NOT NULL DEFAULT 0,
  created_at     timestamptz NOT NULL DEFAULT now(),
  updated_at     timestamptz NOT NULL DEFAULT now()
);

CREATE OR REPLACE TRIGGER trg_user_model_dimension_updated_at
  BEFORE UPDATE ON user_model_dimension
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();

-- ---------------------------------------------------------------------------
-- Self Model: self_model_domain
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS self_model_domain (
  id          integer     GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name        text        NOT NULL UNIQUE,
  predictions integer     NOT NULL DEFAULT 0
              CHECK (predictions >= 0),
  correct     integer     NOT NULL DEFAULT 0
              CHECK (correct >= 0 AND correct <= predictions),
  created_at  timestamptz NOT NULL DEFAULT now(),
  updated_at  timestamptz NOT NULL DEFAULT now()
);

CREATE OR REPLACE TRIGGER trg_self_model_domain_updated_at
  BEFORE UPDATE ON self_model_domain
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();

-- ---------------------------------------------------------------------------
-- Procedural Memory: skill
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS skill (
  id                integer     GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name              text        NOT NULL,
  trigger_context   text        NOT NULL,
  trigger_embedding vector(768),
  strategy          text        NOT NULL,
  success_count     integer     NOT NULL DEFAULT 0,
  attempt_count     integer     NOT NULL DEFAULT 0,
  success_rate      real        GENERATED ALWAYS AS (
    CASE WHEN attempt_count = 0 THEN 0.0
         ELSE success_count::real / attempt_count
    END
  ) STORED,
  version           integer     NOT NULL DEFAULT 1,
  parent_id         integer     REFERENCES skill(id) ON DELETE SET NULL,
  promoted_at       timestamptz,
  domain            text[]      NOT NULL DEFAULT '{}',
  created_at        timestamptz NOT NULL DEFAULT now(),
  updated_at        timestamptz NOT NULL DEFAULT now()
);

-- HNSW index for trigger similarity search
CREATE INDEX IF NOT EXISTS idx_skill_trigger_embedding ON skill
  USING hnsw (trigger_embedding vector_cosine_ops) WITH (m = 16, ef_construction = 64);

-- Active (non-promoted) skills for retrieval
CREATE INDEX IF NOT EXISTS idx_skill_active ON skill (updated_at DESC)
  WHERE promoted_at IS NULL;

-- Version lineage
CREATE INDEX IF NOT EXISTS idx_skill_parent ON skill (parent_id)
  WHERE parent_id IS NOT NULL;

-- Trigger: auto-update updated_at
CREATE OR REPLACE TRIGGER trg_skill_updated_at
  BEFORE UPDATE ON skill
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();
