describe("skill", () => {
	test("insert with defaults yields success_rate = 0.0", async () => {
		const rows = await pool.sql`
			INSERT INTO skill (name, trigger_context, strategy)
			VALUES ('test skill', 'when user asks about weather', 'check weather API')
			RETURNING success_rate
		`;
		expect(rows[0]?.["success_rate"]).toBe(0);
	});

	test("success_rate is computed correctly after update", async () => {
		const inserted = await pool.sql`
			INSERT INTO skill (name, trigger_context, strategy)
			VALUES ('rated skill', 'trigger', 'strategy')
			RETURNING id
		`;
		const skillId = Number(inserted[0]?.["id"]);

		await pool.sql`
			UPDATE skill SET success_count = 3, attempt_count = 4 WHERE id = ${skillId}
		`;

		const rows = await pool.sql`
			SELECT success_rate FROM skill WHERE id = ${skillId}
		`;
		expect(rows[0]?.["success_rate"]).toBeCloseTo(0.75, 5);
	});

	test("version lineage: parent_id FK works", async () => {
		const parent = await pool.sql`
			INSERT INTO skill (name, trigger_context, strategy)
			VALUES ('parent skill', 'trigger', 'strategy v1')
			RETURNING id
		`;
		const parentId = Number(parent[0]?.["id"]);

		const child = await pool.sql`
			INSERT INTO skill (name, trigger_context, strategy, parent_id, version)
			VALUES ('child skill', 'trigger', 'strategy v2', ${parentId}, 2)
			RETURNING id, parent_id
		`;
		expect(child[0]?.["parent_id"]).toBe(parentId);
	});

	test("parent ON DELETE SET NULL", async () => {
		const parent = await pool.sql`
			INSERT INTO skill (name, trigger_context, strategy)
			VALUES ('parent to delete', 'trigger', 'strategy')
			RETURNING id
		`;
		const parentId = Number(parent[0]?.["id"]);

		const child = await pool.sql`
			INSERT INTO skill (name, trigger_context, strategy, parent_id)
			VALUES ('orphaned child', 'trigger', 'strategy', ${parentId})
			RETURNING id
		`;
		const childId = Number(child[0]?.["id"]);

		// Delete parent
		await pool.sql`DELETE FROM skill WHERE id = ${parentId}`;

		// Child's parent_id should be NULL
		const check = await pool.sql`SELECT parent_id FROM skill WHERE id = ${childId}`;
		expect(check[0]?.["parent_id"]).toBeNull();
	});
});

