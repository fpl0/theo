describe("edge", () => {
	let sourceNodeId: number;
	let targetNodeId: number;

	beforeAll(async () => {
		const rowsA = await pool.sql`
			INSERT INTO node (kind, body) VALUES ('fact', 'Edge test node A') RETURNING id
		`;
		sourceNodeId = Number(rowsA[0]?.["id"]);

		const rowsB = await pool.sql`
			INSERT INTO node (kind, body) VALUES ('fact', 'Edge test node B') RETURNING id
		`;
		targetNodeId = Number(rowsB[0]?.["id"]);
	});

	test("FK constraint rejects invalid source_id", async () => {
		try {
			await pool.sql`INSERT INTO edge (source_id, target_id, label) VALUES (999999, ${targetNodeId}, 'related_to')`;
			expect.unreachable("should have thrown");
		} catch (e) {
			expect(e).toBeDefined();
		}
	});

	test("CHECK: rejects weight > 5.0", async () => {
		try {
			await pool.sql`INSERT INTO edge (source_id, target_id, label, weight) VALUES (${sourceNodeId}, ${targetNodeId}, 'test', 6.0)`;
			expect.unreachable("should have thrown");
		} catch (e) {
			expect(e).toBeDefined();
		}
	});

	test("active edge partial indexes exist", async () => {
		const rows = await pool.sql`
			SELECT indexname, indexdef FROM pg_indexes
			WHERE tablename = 'edge'
			  AND indexname IN ('idx_edge_source_active', 'idx_edge_target_active')
			ORDER BY indexname
		`;
		expect(rows).toHaveLength(2);
		expect(String(rows[0]?.["indexdef"])).toContain("valid_to IS NULL");
		expect(String(rows[1]?.["indexdef"])).toContain("valid_to IS NULL");
	});

	test("unique active edge index prevents duplicates", async () => {
		await pool.sql`
			INSERT INTO edge (source_id, target_id, label)
			VALUES (${sourceNodeId}, ${targetNodeId}, 'related_to')
		`;
		try {
			await pool.sql`
				INSERT INTO edge (source_id, target_id, label)
				VALUES (${sourceNodeId}, ${targetNodeId}, 'related_to')
			`;
			expect.unreachable("should have thrown on duplicate active edge");
		} catch (e) {
			expect(e).toBeDefined();
		}
	});

	test("ON DELETE CASCADE removes edges when node is deleted", async () => {
		// Create a pair of nodes with an edge
		const srcRows = await pool.sql`
			INSERT INTO node (kind, body) VALUES ('fact', 'Cascade test A') RETURNING id
		`;
		const srcId = Number(srcRows[0]?.["id"]);

		const tgtRows = await pool.sql`
			INSERT INTO node (kind, body) VALUES ('fact', 'Cascade test B') RETURNING id
		`;
		const tgtId = Number(tgtRows[0]?.["id"]);

		await pool.sql`
			INSERT INTO edge (source_id, target_id, label)
			VALUES (${srcId}, ${tgtId}, 'test_cascade')
		`;

		// Delete source node — edge should be cascaded
		await pool.sql`DELETE FROM node WHERE id = ${srcId}`;

		const remaining = await pool.sql`
			SELECT id FROM edge WHERE source_id = ${srcId} OR target_id = ${srcId}
		`;
		expect(remaining).toHaveLength(0);

		// Cleanup
		await pool.sql`DELETE FROM node WHERE id = ${tgtId}`;
	});
});

